# EXERCISE 10 : Arithmetic
import math

a = int(input('Please enter the former integer: '))
b = int(input('Please enter the latter integer: '))

print(a, '+', b, 'is', a + b)
print(a, '-', b, 'is', a - b)
print(a, '*', b, 'is', a * b)
print(a, '/', b, 'is', a / b)
print(a, '%', b, 'is', a % b)
print('The base 10 logarithm of', a, 'is', math.log10(a))
print(a, 'Ë†', b, 'is', a ** b)
